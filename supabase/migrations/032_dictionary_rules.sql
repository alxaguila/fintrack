-- ============================================================
-- FinTrack — Migración 032: Diccionario de clasificación en BD
-- Ejecutar en el SQL Editor de Supabase (después de 001–031)
-- ============================================================
--
-- Hasta ahora el diccionario integrado de categorización (MERCADONA → super-
-- mercado, NETFLIX → streaming, etc.) vivía como código fuente fijo en
-- src/lib/categoryRules.ts. Esta migración lo traslada a una tabla editable
-- desde /admin/reglas, para poder ver y añadir palabras sin necesidad de
-- desplegar código.
--
-- Cadena de clasificación al importar (sin cambios de orden, ver CLAUDE.md):
--   1. Reglas del propio usuario (keyword_rules)
--   2. Reglas de la comunidad (community_rules)
--   3. Diccionario integrado ← ahora en esta tabla, antes en código
--   4. Sin categoría
--
-- `applies_to_bizum`: replica el comportamiento de ALWAYS_RULES del código
-- original — normalmente un Bizum entre personas no se clasifica por
-- diccionario (podría ser cualquier cosa), salvo unas pocas palabras muy
-- fiables (NOMINA, PARKING, COMUNIDAD) que si aparecen, se respetan siempre.
--
-- Lectura para cualquier autenticado (se usa en el import de cualquier
-- usuario). Escritura solo admin (is_admin(), migración 015).
--
-- Idempotente: se puede re-ejecutar sin error (el INSERT de semilla usa
-- ON CONFLICT DO NOTHING sobre `pattern`, que es UNIQUE).
-- ------------------------------------------------------------

-- ------------------------------------------------------------
-- 1. Tabla dictionary_rules
-- ------------------------------------------------------------
CREATE TABLE IF NOT EXISTS dictionary_rules (
  id                uuid        NOT NULL DEFAULT gen_random_uuid() PRIMARY KEY,
  category_id       uuid        NOT NULL REFERENCES categories(id) ON DELETE CASCADE,
  pattern           text        NOT NULL,
  applies_to_bizum  boolean     NOT NULL DEFAULT false,
  sort_order        integer     NOT NULL DEFAULT 0,
  created_at        timestamptz NOT NULL DEFAULT now(),
  UNIQUE (pattern)
);

ALTER TABLE dictionary_rules DROP CONSTRAINT IF EXISTS chk_dict_pattern_len;
ALTER TABLE dictionary_rules ADD  CONSTRAINT chk_dict_pattern_len
  CHECK (char_length(pattern) BETWEEN 2 AND 80);

-- Los patrones se guardan y comparan siempre en mayúsculas (igual que
-- normalize() en categoryRules.ts); esto evita duplicados tipo "Mercadona" /
-- "MERCADONA" que no se detectarían con el UNIQUE de arriba.
ALTER TABLE dictionary_rules DROP CONSTRAINT IF EXISTS chk_dict_pattern_upper;
ALTER TABLE dictionary_rules ADD  CONSTRAINT chk_dict_pattern_upper
  CHECK (pattern = upper(pattern));

CREATE INDEX IF NOT EXISTS idx_dictionary_rules_sort ON dictionary_rules (sort_order);
CREATE INDEX IF NOT EXISTS idx_dictionary_rules_category ON dictionary_rules (category_id);

ALTER TABLE dictionary_rules ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "dictionary_rules read" ON dictionary_rules;
CREATE POLICY "dictionary_rules read"
  ON dictionary_rules FOR SELECT TO authenticated
  USING (true);

DROP POLICY IF EXISTS "dictionary_rules admin insert" ON dictionary_rules;
CREATE POLICY "dictionary_rules admin insert"
  ON dictionary_rules FOR INSERT TO authenticated
  WITH CHECK (public.is_admin());

DROP POLICY IF EXISTS "dictionary_rules admin update" ON dictionary_rules;
CREATE POLICY "dictionary_rules admin update"
  ON dictionary_rules FOR UPDATE TO authenticated
  USING (public.is_admin()) WITH CHECK (public.is_admin());

DROP POLICY IF EXISTS "dictionary_rules admin delete" ON dictionary_rules;
CREATE POLICY "dictionary_rules admin delete"
  ON dictionary_rules FOR DELETE TO authenticated
  USING (public.is_admin());

GRANT SELECT, INSERT, UPDATE, DELETE ON dictionary_rules TO authenticated;

